package loopP1;

public class Sample1 {

	public static void main(String[] args) {
		
		int i;
		for(i=0;i<5;i++)
		{
			if(i%2==0)
			{
				System.out.println("Even value is::"+i);
			}
			else
			{
				System.out.println("Odd value is::"+i);
			}
			//System.out.println("I value is::"+i);
		}

	}

}
